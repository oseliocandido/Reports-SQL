4151)
)


SELECT 
       F.CODFORNEC,
       F.FORNECEDOR,
       F.CODFORNEC ||' - ' || F.FORNECEDOR AS CONCAT_FORNECEDOR,
       SEC.CODSEC,
       SEC.DESCRICAO SECAO,
       SEC.CODSEC ||' - ' || SEC.DESCRICAO AS CONCAT_SECAO ,
       P.CODPROD,
       P.DESCRICAO Produto,
       P.CODPROD ||' - ' || P.DESCRICAO AS CONCAT_PRODUTO,
       P.OBS2 ATIVO,
       CASE WHEN P.CODFORNEC IN (SELECT * FROM LISTA_FORNECEDORES) THEN 'Varejo'
             ELSE 'Outros'END Tipo_Fornecedor
                  
FROM PCPRODUT P 

INNER JOIN PCFORNEC F
ON P.CODFORNEC = F.CODFORNEC

INNER JOIN PCSECAO SEC
ON P.CODSEC = SEC.CODSEC